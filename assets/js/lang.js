/**
 * Initialize language from localStorage or default to English
 */
let currentLang = localStorage.getItem('gs_lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('gs_lang', lang);
    applyTranslations();
    
    // Update active state of buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

function applyTranslations() {
    const langData = translations[currentLang];
    document.querySelectorAll('[data-t]').forEach(el => {
        const key = el.dataset.t;
        if (langData[key]) {
            if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'password' || el.type === 'search')) {
                el.placeholder = langData[key];
            } else {
                el.innerText = langData[key];
            }
        }
    });
    
    // Set HTML dir attribute if needed (not needed for these languages but good practice)
    document.documentElement.lang = currentLang;
}

// Run on page load
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    
    // Set active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === currentLang) btn.classList.add('active');
        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });
});
